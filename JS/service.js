

function ampliar(){ 
    document.querySelector('.amplia img').style. transition = '2s'
    document.querySelector('.amplia').style.display = 'block'
    document.querySelector('.amplia img').style.width = '80%'
   
}

function ampliarFecha(){ 
    document.querySelector('.amplia img').style. transition = '2s'
    document.querySelector('.amplia').style.display = 'none'
    document.querySelector('.amplia img').style.width = '80%'
   
}

